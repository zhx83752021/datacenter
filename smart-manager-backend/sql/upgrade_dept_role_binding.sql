-- 17. 验证查询
-- ===========================================================================================

-- 科室树结构验证
SELECT '===== 科室树结构 =====' AS section;
SELECT
    id,
    parent_id,
    CONCAT(
        CASE LENGTH(ancestors) - LENGTH(REPLACE(ancestors, ',', ''))
            WHEN 0 THEN ''
            WHEN 1 THEN '├─ '
            WHEN 2 THEN '│  ├─ '
            WHEN 3 THEN '│  │  ├─ '
            ELSE '│  │  │  ├─ '
        END,
        dept_name
    ) AS dept_tree,
    ancestors
FROM sys_dept
WHERE del_flag = '0'
ORDER BY ancestors, order_num;

-- 用户-角色-科室绑定验证
SELECT '===== 用户权限绑定 =====' AS section;
SELECT
    u.username,
    u.real_name,
    d.dept_name AS '所属科室',
    r.role_name AS '角色',
    r.role_key,
    CASE r.data_scope
        WHEN '1' THEN '全部数据'
        WHEN '2' THEN '自定义'
        WHEN '3' THEN '本部门'
        WHEN '4' THEN '部门及以下'
        WHEN '5' THEN '仅本人'
    END AS '数据权限范围'
FROM sys_user u
LEFT JOIN sys_dept d ON u.dept_id = d.id
LEFT JOIN sys_user_role ur ON u.id = ur.user_id
LEFT JOIN sys_role r ON ur.role_id = r.id
WHERE u.del_flag = 0
ORDER BY u.id;

-- 统计
SELECT '===== 科室统计 =====' AS section;
SELECT
    COUNT(*) AS '科室总数',
    SUM(CASE WHEN LENGTH(ancestors) - LENGTH(REPLACE(ancestors, ',', '')) = 1 THEN 1 ELSE 0 END) AS '院区数',
    SUM(CASE WHEN LENGTH(ancestors) - LENGTH(REPLACE(ancestors, ',', '')) = 2 THEN 1 ELSE 0 END) AS '科系数',
    SUM(CASE WHEN LENGTH(ancestors) - LENGTH(REPLACE(ancestors, ',', '')) >= 3 THEN 1 ELSE 0 END) AS '末级科室数'
FROM sys_dept WHERE del_flag = '0' AND id != 100;
